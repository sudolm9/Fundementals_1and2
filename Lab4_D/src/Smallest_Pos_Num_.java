
public class Smallest_Pos_Num_<sudolm9> {

	public static void main(String[] args) {
		long g = 16;
		while (1==1)
		{
			boolean tst = true;
			for(int i = 1; i < 16; i++) //range from 1 to 16
			{
			
			tst = true;
			if(g%i != 0)
			{
				tst = false;
				break;
			}
	}
		if(tst)
		{
			System.out.println(g);
			System.exit(0); // stops the program
		}
		g++;
}
}
}

